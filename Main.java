
public class Main {

	public static void main(String[] args) {
		System.out.println("Java101\b başlıyoruz");			// Yazılan kadar gerisindeki harfi siler
		
		System.out.println("Java101\n başlıyoruz");			// Bir satır aşağı geçer
		
		System.out.println("Java101\t başlıyoruz");			// Bir tab boşluk ekler
			
		System.out.println("Java101\r başlıyoruz");			// Yazılan yerden sonrasını satır başı kabul eder
		
		System.out.println("Java101\' de başlıyoruz");		// Kesme işareti için
		
		System.out.println("Java101 \"başlıyoruz\" ");		// Çift tırnak içerisine alır
		
		System.out.println("Java101 \\başlıyoruz\\metin.txt");			// Tek slash yapar
	

	}

}
